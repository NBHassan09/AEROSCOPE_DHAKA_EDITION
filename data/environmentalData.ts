const parseData = (rawData: string): TimeSeriesDataPoint[] => {
  return rawData.split('\n').map(line => {
    const [year, month, no2, ntl] = line.split('\t');
    const monthPadded = month.padStart(2, '0');
    return {
      year: parseInt(year),
      month: parseInt(month),
      date: `${year}-${monthPadded}`,
      no2: parseFloat(no2),
      ntl: parseFloat(ntl),
    };
  });
};

